# —ç—Ç–æ –∫–∞—Ç–∞–ª–æ–≥ —Ç–æ–≤–∞—Ä–æ–≤. –≤ –∫–ª—é—á–µ –Ω–∞–∏–º–µ–Ω–æ–≤–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–∞, –≤ –∑–Ω–∞—á–µ–Ω–∏–∏ —Å—Ç–æ–∏–º–æ—Å—Ç—å —Ç–æ–≤–∞—Ä–∞
catalog = {
    "—Ä—É–±–∞—à–∫–∞": 4000,
    "–∫—Ä–µ—Å–ª–æ": 10000,
    "—é–±–∫–∞": 5000,
    "—Å—Ç–∞–∫–∞–Ω": 500,
    "–±—Ä—é–∫–∏": 2500,
    "–±–ª—É–∑–∫–∞": 3000,
    "—Å—Ç—É–ª": 5000,
    "–∫—Ä—É–∂–∫–∞": 500,
}

# —ç—Ç–æ –∫–æ—Ä–∑–∏–Ω–∞ —Å —Ç–æ–≤–∞—Ä–∞–º–∏. –≤ –∫–ª—é—á–µ –Ω–∞–∏–º–µ–Ω–æ–≤–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–∞, –≤ –∑–Ω–∞—á–µ–Ω–∏–∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ç–æ–≤–∞—Ä–∞
cartOne = {
    "—Ä—É–±–∞—à–∫–∞": 3,
    "–±—Ä—é–∫–∏": 2,
    "–∫—Ä–µ—Å–ª–æ": 1,
}

cartTwo = {
    "—Å—Ç—É–ª": 6,
    "—Å—Ç–∞–∫–∞–Ω": 12,
    "–∫—Ä—É–∂–∫–∞": 12,
}

# –Ω–∞–ø–∏—Å–∞—Ç—å —Ñ—É–Ω–∫—Ü–∏—é, –∫–æ—Ç–æ—Ä–∞—è —Ä–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ—Ç —Å—Ç–æ–∏–º–æ—Å—Ç—å –∫–æ—Ä–∑–∏–Ω—ã
def costOfCart(cart):
    y1 = 0
    for i in catalog:
        for x1 in cart:
            if i == x1:
                y1 = y1 + (catalog[i] * cart[x1])

    return y1


print(costOfCart(cartOne))
print(costOfCart(cartTwo))